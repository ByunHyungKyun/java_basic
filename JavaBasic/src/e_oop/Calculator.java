package e_oop;

public class Calculator {

	//다섯개의 메서드 만들기(+,-,*,/,%)
	//각각의 메서드는 2개의파라미터를 받아야한다
	//받은결과를 리턴해주면 된다 
	
	
	
	
	double sum(double a,double b){ //더하기 메서드
		return a+b;
	}
	double bba(double a,double b){//빼기 메서드
		return a-b;
	}
	double gob(double a,double b){//곱하기 메서드
		return a*b;
	}
	double na(double a,double b){//나누기 메서드
		return a/b;
	}
	double nam(double a,double b){//나머지 메서드
		return a%b;
	}
	
	
	
	
	
}
